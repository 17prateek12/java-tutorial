public class ByteTest {
    public static void main(String args[]){
        byte a = 50;
        byte t = -100;

        System.out.println("Age "+a); // Age 5
        System.out.println("Temperature "+t);  // Temperature -10
        System.out.println("Aremethic operation in byte "+ (a+t)); // Aremethic operation in byte -5
        System.out.println("Aremethic operation in byte "+ (a-t)); // Aremethic operation in byte 15
        System.out.println("Aremethic operation in byte "+ (a*t)); // Aremethic operation in byte -50
        System.out.println("Aremethic operation in byte "+ (a/t)); // Aremethic operation in byte 0
        System.out.println("Aremethic operation in byte "+ a+t); // Aremethic operation in byte 5-10

    }
}


